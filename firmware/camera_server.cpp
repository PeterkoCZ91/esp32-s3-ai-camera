static esp_err_t health_handler(httpd_req_t *req) {
    esp_task_wdt_reset(); // Keep WDT happy in detection stream
    
    httpd_resp_set_type(req, "application/json");
    httpd_resp_set_hdr(req, "Access-Control-Allow-Origin", "*");
    
    // FIXED: Use StaticJsonDocument to avoid heap fragmentation
    StaticJsonDocument<1024> doc;
    
    // Uptime
    unsigned long uptimeSeconds = millis() / 1000;
    doc["uptime_seconds"] = uptimeSeconds;
    doc["uptime_hours"] = (uptimeSeconds % 86400) / 3600;
    doc["uptime_days"] = uptimeSeconds / 86400;
    
    // Memory
    doc["free_heap"] = ESP.getFreeHeap();
    doc["free_psram"] = ESP.getFreePsram();
    doc["heap_health"] = ESP.getFreeHeap() > 50000 ? "healthy" : 
                        (ESP.getFreeHeap() > 30000 ? "warning" : "critical");
    
    // WiFi
    if (WiFi.status() == WL_CONNECTED) {
        doc["wifi_connected"] = true;
        doc["wifi_rssi"] = WiFi.RSSI();
        doc["wifi_quality"] = getWiFiQuality(WiFi.RSSI());
        doc["wifi_reconnects_1h"] = wifi_health.reconnect_count;
    } else {
        doc["wifi_connected"] = false;
        doc["wifi_quality"] = "disconnected";
    }
    
    // System Stats
    doc["total_restarts"] = sys_stats.total_restarts;
    doc["last_restart_reason"] = sys_stats.last_restart_reason;
    
    // Overall health status
    bool healthy = true;
    String issues = "";
    
    if (ESP.getFreeHeap() < 50000) {
        healthy = false;
        issues += "low_memory,";
    }
    if (WiFi.RSSI() < -75) {
        healthy = false;
        issues += "weak_wifi,";
    }
    if (wifi_health.reconnect_count > 3) {
        healthy = false;
        issues += "unstable_wifi,";
    }
    
    doc["overall_health"] = healthy ? "healthy" : "degraded";
    doc["issues"] = issues.length() > 0 ? issues.substring(0, issues.length()-1) : "none";
    doc["status"] = healthy ? "ok" : "warning";
    
    String response;
    serializeJson(doc, response);
    httpd_resp_sendstr(req, response.c_str());
    return ESP_OK;
}

